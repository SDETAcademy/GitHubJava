package day_39;

public class BestBuy {
    /**
     * Please use encapsulation OOP Principle.
     * Declare private variables phone, tablet, tv, soundbar, webcam, mic.
     * Please allow user to set and get these variable values.
     */

    private String phone;
    private String tablet;
    private String tv;
    private String soundbar;
    private String webcam;
    private String mic;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getTablet() {
        return tablet;
    }

    public void setTablet(String tablet) {
        this.tablet = tablet;
    }

    public String getTv() {
        return tv;
    }

    public void setTv(String tv) {
        this.tv = tv;
    }

    public String getSoundbar() {
        return soundbar;
    }

    public void setSoundbar(String soundbar) {
        this.soundbar = soundbar;
    }

    public String getWebcam() {
        return webcam;
    }

    public void setWebcam(String webcam) {
        this.webcam = webcam;
    }

    public String getMic() {
        return mic;
    }

    public void setMic(String mic) {
        this.mic = mic;
    }

}
